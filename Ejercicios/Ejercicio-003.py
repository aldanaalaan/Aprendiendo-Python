# Operadores aritmeticos

# SUMA
print('SUMA:')
num1 = 5
num2 = 4
resultado = num1 + num2

print('El resultado de la suma de {} y {} es: {}'.format(num1, num2, resultado))

# RESTA
print('RESTA:')
num1 = 9
num2 = 5
resultado = num1 - num2

print('El resultado de la resta de {} y {} es: {}'.format(num1, num2, resultado))

# MULTIPLICACION
print('MULTIPLICACION:')
num1 = 5
num2 = 7
resultado = num1 * num2

print('El resultado de la multiplicacion de {} y {} es: {}'.format(num1, num2, resultado))

# DIVISION
print('DIVISION:')
num1 = 15.7
num2 = 4
resultado = num1 / num2

print('El resultado de la division de {} y {} es: {}'.format(num1, num2, resultado))

# DIVISION entera
print('DIVISION entera:')
resultado = num1 // num2

print('El resultado de la division entera de {} y {} es: {}'.format(num1, num2, resultado))

# POTENCIA
print('POTENCIA:')
num1 = 8
num2 = 3
resultado = num1 ** num2

print('El resultado de elevar {} a la potencia {} es: {}'.format(num1, num2, resultado))

# MODULO
print('MODULO:')
num1 = 19
num2 = 7
resultado = num1 % num2

print('El resto al dividir {} entre {} es: {}'.format(num1, num2, resultado))